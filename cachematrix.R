## This set of functions shows how lexical scoping in R helps to 
## simplify certain applied tasks such as data caching

## This function creates an object which contains methods (gets and sets) 
## and state (cached inverse matrix stored in s)

makeCacheMatrix <- function(x = matrix()) {
  s <- NULL
  set <- function(y) {
    x <<- y
    s <<- NULL
  }
  get <- function() x
  setsolve <- function(solve) s <<- solve
  getsolve <- function() s
  list(set = set, get = get,
       setsolve = setsolve,
       getsolve = getsolve)
}


## This function checks whether inverse matrix is cached, 
##if yes retuns it and if not calculates it.

cacheSolve <- function(x, ...) {
  s <- x$getsolve()
  if(!is.null(s)) {
    message("getting cached data")
    return(s)
  }
  data <- x$get()
  s <- solve(data)
  x$setsolve(s)
  s
}
